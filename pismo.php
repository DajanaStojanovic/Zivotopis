<br /><br />
Dobrovoljno vatrogasno društvo Branjin Vrh je malo vatrogasno društvo u srcu Baranje koje djeluje već 117 godina. Kako društvo trenutno nema svoju web stranicu, putem ovog projekta bih napravila web stranicu dobrovoljnog vatrogasnog društva. Na toj bi stranici krajnji korisnici (posjetitelji) mogli pratiti sva događanja, čitati vijesti vezane za ovo, ali i druga dobrovoljna vatrogasna društva u Baranji i šire. Korisnici bi mogli kontaktirati društvo pomoću mail adrese, odnosno obrasca koji se šalje na mail adresu te se upisati u vatrogasno društvo putem prijavnice dostupne na web stranici. S druge strane poslužitelji informacija mogu vrlo jednostavno najavljivati događanja, objavljivati vijesti, postaviti fotografije i sl.
